-- Standard (ready-made) gi prices: one row per (model, fit, size), full set only.
-- JPY, tax included. Only rows that exist are loaded — sizes shown with a dash
-- in the price list are simply absent, so they're naturally unselectable.
-- The (fit, size_code) FK guarantees every priced size exists in its chart.
-- Public read-only; writes via migrations / admin.

create table gi_standard_prices (
  model_slug text          not null references gi_models(slug),
  fit        gi_size_chart not null,           -- 'slim' or 'normal'
  size_code  text          not null,
  price      integer       not null,           -- JPY, tax incl., full set
  primary key (model_slug, fit, size_code),
  foreign key (fit, size_code) references size_charts (chart, size_code)
);

