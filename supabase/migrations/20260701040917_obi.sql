-- ============================================================================
-- Block 4 — OBI (belts)
-- Chain: color -> material -> width -> size. All prices JPY, tax included.
-- Public read-only; writes via migrations / admin.
--
-- Material codes: nami = plain cotton (algodón normal), shushi = satin (satinado),
--                 yohachi = special cotton (algodón especial), silk = silk (seda).
-- Width: 4.0 cm (normal) and 4.5 cm (special). Nami is 4.0 only.
-- Special (4.5) exists from size 3 onward: sizes 0,1,2 are NULL by design.
-- The color->material->width chain is DERIVED from which rows exist here:
--   a material/width is offered only if it has rows; a size is offered only if
--   its price is not NULL. No separate availability tables needed.
-- ============================================================================

create type obi_color    as enum ('black','blue','red','white','green','yellow','purple','orange','brown');
create type obi_material as enum ('nami','shushi','yohachi','silk');

-- Size -> total length in cm (0..13).
create table obi_sizes (
  size_code  smallint primary key,
  length_cm  smallint not null
);

