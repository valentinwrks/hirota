-- Obi embroidery: per-character price by width x thread (reuses gi_thread enum).
-- Two bordable ends (A and B) are handled as character count in the engine.
-- Note: white/colored (non-black) belts are embroidered in black thread only —
-- a display rule handled in the engine/UI, not a price difference.
create table obi_embroidery_prices (
  width_cm       numeric(2,1) not null,
  thread         gi_thread    not null,
  price_per_char integer      not null,
  primary key (width_cm, thread)
);

