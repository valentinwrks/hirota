-- Price per (color, material, width, size). NULL price = size not offered for
-- that combo, but the row is kept so a future size is a one-cell UPDATE.
create table obi_prices (
  color     obi_color    not null,
  material  obi_material not null,
  width_cm  numeric(2,1) not null,          -- 4.0 or 4.5
  size_code smallint     not null references obi_sizes(size_code),
  price     integer,                        -- JPY, tax incl.; NULL = not offered
  primary key (color, material, width_cm, size_code)
);

